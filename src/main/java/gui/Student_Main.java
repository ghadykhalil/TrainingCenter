    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        // TODO add your handling code here:
        JTabbedPane sourceTabbedPane = (JTabbedPane) evt.getSource();
        int selectedIndex = sourceTabbedPane.getSelectedIndex();

        if (selectedIndex == 1) { // Checking if "Settings" tab is clicked
            DefaultComboBoxModel<Subject> subjectModelCbx = new DefaultComboBoxModel<>();
            List<Subject> currentSubjects = EnrollController.getAvailableSubjects(loggedStudent);
            subjectModelCbx.addElement(null);
            for (Subject subject : currentSubjects) {
                subjectModelCbx.addElement(subject);
            }
            selectSubjectCbx.setModel(subjectModelCbx);
        }
        if (selectedIndex == 2) {
            DefaultListModel<Enroll> pendingEnrollModel = new DefaultListModel<>();
            List<Enroll> pendingEnroll = EnrollController.getDuePaymentsForStudent(loggedStudent.getId());
            pendingEnrollModel.addAll(pendingEnroll);
            pendingPaymentList.setModel(pendingEnrollModel);
            DefaultListModel<Enroll> completedEnrollementListModel = new DefaultListModel<>();
            List<Enroll> completedEnrollement = EnrollController.getFinishedPayment(loggedStudent.getId());
            completedEnrollementListModel.addAll(completedEnrollement);
            completedEnrollementList.setModel(completedEnrollementListModel);
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

