    private void selectedPaymentMethodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectedPaymentMethodActionPerformed
        // TODO add your handling code here:
        // Clear previous content
        dynamicPaymentMethodPanel.removeAll();
        dynamicPaymentMethodPanel.setLayout(new GridBagLayout());
        GridBagConstraints gbc = new GridBagConstraints();
        gbc.gridx = 0;
        gbc.fill = GridBagConstraints.HORIZONTAL;
        gbc.insets = new Insets(5, 5, 5, 5); // Padding

        // Handle selected payment method
        switch ((String) selectedPaymentMethod.getSelectedItem()) {
            case "VisaCard":
                // VisaCard Payment Form UI
                JLabel visaCardLabel = new JLabel("VisaCard Number");
                visaCardLabel.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 0;
                dynamicPaymentMethodPanel.add(visaCardLabel, gbc);

                JTextField visaCardTextField = new JTextField(20);
                visaCardTextField.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 1;
                dynamicPaymentMethodPanel.add(visaCardTextField, gbc);

                JLabel visaCardCvcLabel = new JLabel("Three-Digit Code (CVC)");
                visaCardCvcLabel.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 2;
                dynamicPaymentMethodPanel.add(visaCardCvcLabel, gbc);

                JTextField visaCardCvcField = new JTextField(4);
                visaCardCvcField.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 3;
                dynamicPaymentMethodPanel.add(visaCardCvcField, gbc);

                JButton visaCardButton = new JButton("Proceed with VisaCard");
                visaCardButton.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 4;
                visaCardButton.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        String cardNumber = visaCardTextField.getText();
                        String cvc = visaCardCvcField.getText();
                        VisaCard visaCard = (VisaCard) PaymentMethodFactory.createPaymentMethod(paymentMethod.VisaCard, null, null, cardNumber, cardNumber);
                        // SELECTING ENROLL
                        Enroll selectedEnroll = pendingPaymentList.getSelectedValue();
                        // UPDATING PAYMENT
                        Payment newPayment = selectedEnroll.getPayment();
                        newPayment.setPaymentMethod(paymentMethod.VisaCard);
                        newPayment.setPaymentMethodId(visaCard.getId());
                        newPayment.setStatus(true);
                        PaymentController.updatePayment(newPayment);
                        selectedEnroll.setPayment(newPayment);
                        selectedEnroll.setStatus(enrollStatus.Active);
                        EnrollController.updateEnrollment(selectedEnroll);
                        VisaCardController.addVisaCard(visaCard);
                        visaCardTextField.setText(null);
                        visaCardCvcField.setText(null);
                    }
                });
                dynamicPaymentMethodPanel.add(visaCardButton, gbc);
                break;

            case "PayPal":
                // PayPal Payment Form UI
                JLabel payPalLabel = new JLabel("PayPal Username");
                payPalLabel.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 0;
                dynamicPaymentMethodPanel.add(payPalLabel, gbc);

                JTextField payPalUsernameField = new JTextField(20);
                payPalUsernameField.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 1;
                dynamicPaymentMethodPanel.add(payPalUsernameField, gbc);

                JLabel payPalPasswordLabel = new JLabel("PayPal Password");
                payPalPasswordLabel.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 2;
                dynamicPaymentMethodPanel.add(payPalPasswordLabel, gbc);

                JPasswordField payPalPasswordField = new JPasswordField(20);
                payPalPasswordField.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 3;
                dynamicPaymentMethodPanel.add(payPalPasswordField, gbc);

                JButton payPalButton = new JButton("Proceed with PayPal");
                payPalButton.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 4;
                payPalButton.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        String username = payPalUsernameField.getText();
                        String password = new String(payPalPasswordField.getPassword());
                        // Handle PayPal payment logic
                        Paypal paypal = (Paypal) PaymentMethodFactory.createPaymentMethod(paymentMethod.Paypal, username, password, null, null);
                        // SELECTING ENROLL
                        Enroll selectedEnroll = pendingPaymentList.getSelectedValue();
                        // UPDATING PAYMENT
                        Payment newPayment = selectedEnroll.getPayment();
                        newPayment.setPaymentMethod(paymentMethod.Paypal);
                        newPayment.setPaymentMethodId(paypal.getId());
                        newPayment.setStatus(true);
                        PaymentController.updatePayment(newPayment);
                        selectedEnroll.setPayment(newPayment);
                        selectedEnroll.setStatus(enrollStatus.Active);
                        EnrollController.updateEnrollment(selectedEnroll);
                        PayPalController.addPaypal(paypal);
                        payPalUsernameField.setText(null);
                        payPalPasswordField.setText(null);
                    }
                });
                dynamicPaymentMethodPanel.add(payPalButton, gbc);
                break;

            default:
                // Default message if no payment method is selected
                JLabel defaultLabel = new JLabel("Please select a payment method.");
                defaultLabel.setFont(new Font("Arial", Font.PLAIN, 18));
                gbc.gridy = 0;
                dynamicPaymentMethodPanel.add(defaultLabel, gbc);
                break;
        }
        
        // Revalidate and repaint to update the panel
        dynamicPaymentMethodPanel.revalidate();
        dynamicPaymentMethodPanel.repaint();
    }//GEN-LAST:event_selectedPaymentMethodActionPerformed

