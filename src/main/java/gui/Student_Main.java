    public Student_Main(Student student) {
        this();
        this.loggedStudent = student;
        this.dynamicUsername.setText(student.getUsername());
        List<Enroll> enrollements = EnrollController.getEnrollmentsByStudent(loggedStudent.getId());
        DefaultListModel<Subject> enrolledSubjectsModel = new DefaultListModel<>();
        for (Enroll enroll : enrollements) {
            enrolledSubjectsModel.addElement(enroll.getSubject());
        }
        enrolledSubjectList.setModel(enrolledSubjectsModel);
    }

